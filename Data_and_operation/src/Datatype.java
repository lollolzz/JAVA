public class Datatype{
	
	public static void main(String[] args) {
		
		System.out.println(6); // number
		System.out.println("six"); // string (문자열)
		System.out.println("6"); // string 이다. 
		
		System.out.println(6+6); // 12
		System.out.println("6"+"6"); // 66, 문자열 끼리 + 하면 결합 연산이 됨 
		
		System.out.println(6*6);
		//System.out.println("6"*"6");
		
		System.out.println("1111".length()); // string의 길이를 알려주는 연산 
		//System.out.println(1111.length()); // 숫자의 길이를 알려주는 length는 없다 
		
		
		
		
		
		
		
	}
}
