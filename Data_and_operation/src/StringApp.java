public class StringApp {

	public static void main(String[] args) {
		
		System.out.println("Hello World"); // String 
		//System.out.println('Hello World'); // -> '' 는 Character(한 문자 표현)를 표현 
		System.out.println('H');
		
		System.out.println("Hello \nWorld"); // \n 줄 바꿈 => new line의 약자 
		System.out.println("Hello \"World\""); // \ 이스케이프 문자
		
		

	}

}
